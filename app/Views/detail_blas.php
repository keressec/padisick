      <ol>Untuk mengobati penyakit blas pada tanaman padi, umumnya digunakan pendekatan pengendalian yang terpadu. Berikut adalah beberapa langkah yang dapat diambil:

        <li>Penggunaan fungisida: Fungisida adalah zat kimia yang digunakan untuk mengendalikan infeksi jamur. Pilihlah fungisida yang direkomendasikan oleh ahli pertanian atau petugas pertanian setempat. Ikuti petunjuk penggunaan yang tertera pada kemasan fungisida dengan cermat, termasuk dosis yang tepat dan frekuensi pengaplikasian.</li>

        <li>Penggunaan varietas tahan blas: Pilih varietas padi yang memiliki ketahanan terhadap penyakit blas. Varian yang tahan atau resisten terhadap blas akan lebih mampu melawan infeksi jamur dan mengurangi kerusakan pada tanaman.</li>

        <li>Pengelolaan lingkungan: Pastikan tanaman padi mendapatkan kondisi lingkungan yang optimal untuk pertumbuhan. Hal ini meliputi penyediaan pasokan air yang cukup, pemupukan yang seimbang, dan perhatian terhadap sanitasi lingkungan. Tanaman yang sehat memiliki kemampuan bertahan yang lebih baik terhadap penyakit.</li>

        <li>Praktik rotasi tanaman: Rotasi tanaman dapat membantu mengurangi kepadatan inokulum blas di tanah. Hindari menanam tanaman padi secara berkelanjutan di lahan yang sama, sehingga waktu yang diperlukan bagi jamur untuk bertahan hidup dalam tanah menjadi terbatas.</li>

        <li>Pemangkasan dan penghapusan bagian tanaman yang terinfeksi: Jika terdapat daun atau bagian tanaman padi yang terinfeksi blas, segera lakukan pemangkasan dan penghapusan bagian tersebut. Hal ini dapat membantu menghentikan penyebaran penyakit ke bagian tanaman yang sehat.</li>

        <li>Kebersihan dan sanitasi: Pastikan area sekitar tanaman padi tetap bersih dari sisa-sisa tanaman atau material organik lainnya yang dapat menjadi sumber infeksi. Hal ini meliputi penghapusan sisa-sisa tanaman setelah panen, pengelolaan gulma, dan menjaga kebersihan alat-alat pertanian yang digunakan.</li>
      </ol>

