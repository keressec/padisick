        <li>Lesi daun: Bercak-bercak pada daun yang terinfeksi dapat berkembang menjadi lesi berwarna coklat dengan tepi yang lebih gelap. Lesi ini biasanya memiliki bentuk oval atau lonjong.</li>
        <li>Infeksi batang: Blas juga dapat menginfeksi batang tanaman padi. Pada batang yang terinfeksi, dapat terbentuk lesi berwarna coklat dengan bentuk memanjang.</li>
        <li>Kematangan malai terganggu: Infeksi blas yang parah dapat mengganggu perkembangan malai padi. Hal ini dapat menyebabkan pengisian bulir yang tidak sempurna, ukuran malai yang kecil, dan berat gabah yang rendah.</li>
      </ol>
